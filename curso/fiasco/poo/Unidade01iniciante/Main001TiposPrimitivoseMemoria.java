package curso.fiasco.poo.Unidade01iniciante;

public class Main001TiposPrimitivoseMemoria {
    public static void main(String[] args) {
        /* Tipos primitivos */

        // Toda variável em Java deve ter o ponto e vírgula no final ';'
        int idade = 28; // 32 bits
        double altura = 1.80; // 16 bits
        char sexo = 'M'; // 16 bits
        Long id = 2847412412444421L; // 64 bits
        // O int armazena no máximo até 2147483647, após esse valor faz se necessário a utilização do Long
        // O Long sempre possui um 'L' no final do número

    }
}

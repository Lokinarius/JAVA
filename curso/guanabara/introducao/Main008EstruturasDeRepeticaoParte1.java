package curso.guanabara.introducao;

public class Main008EstruturasDeRepeticaoParte1 {
    public static void main(String[] args) {

        // ESTRUTURA DE REPETIÇÃO WHILE
        int j = 0;
        System.out.println("while (j < 10)");
        while (j < 10) {
            System.out.println("Iteração " + (j + 1));
            j++;
        }
    }
}
